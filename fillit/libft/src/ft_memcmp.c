#include "libft.h"

int	ft_memcmp(int *str1, int *str2, size_t n)
{
	size_t			i;

	i = 0;
	while (i < n && str1[i] == str2[i])
		i++;
	if (i == n)
		return (1);
	else
		return (0);
}
