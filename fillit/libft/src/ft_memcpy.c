#include "libft.h"

int		**ft_memcpy(int **dest, const int **src, size_t n)
{
	size_t			i;

	i = 0;
	while (i < n)
	{
		dest[0][i] = src[0][i];
		dest[1][i] = src[1][i];
		i++;
	}
	return (dest);
}
